package org.emample.log.config;
/**
 * 日志功能的配置类
 */
public class LogConfig {

    /**
     * 是否显示时间
     */
    private boolean showTime;

    // 对应getter和setter方法
    public boolean isShowTime() {
        return showTime;
    }

    public void setShowTime(boolean showTime) {
        this.showTime = showTime;
    }

}


